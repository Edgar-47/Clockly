def _reset_identity_sequences(connection) -> None:
    for table_name in (
        "users", "employees", "attendance_sessions", "time_entries",
        "work_schedules", "schedule_days", "employee_schedules",
        "business_users", "plans", "subscriptions",
        "expenses", "expense_attachments",
    ):
        connection.execute(
            f"""
            SELECT setval(
                pg_get_serial_sequence('{table_name}', 'id'),
                COALESCE((SELECT MAX(id) FROM {table_name}), 0) + 1,
                FALSE
            )
            """
        )


def _seconds_between(start, end) -> int:
    try:
        started = (
            start if isinstance(start, datetime) else datetime.fromisoformat(str(start))
        )
        ended = end if isinstance(end, datetime) else datetime.fromisoformat(str(end))
    except (TypeError, ValueError):
        return 0
    return max(int((ended - started).total_seconds()), 0)
