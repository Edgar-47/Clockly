def _execute_schema(connection) -> None:
    """
    Execute schema statements one by one.

    psycopg does not execute multi-statement schema strings with bound params.
    Splitting keeps initialization explicit and easy to debug.
    """
    for statement in SCHEMA_SQL.split(";"):
        clean_statement = statement.strip()
        if clean_statement:
            connection.execute(clean_statement)


def _table_columns(connection, table_name: str) -> set[str]:
    rows = connection.execute(
        """
        SELECT column_name
        FROM information_schema.columns
        WHERE table_schema = CURRENT_SCHEMA()
          AND table_name = %s
        """,
        (table_name,),
    ).fetchall()
    return {row["column_name"] for row in rows}


def _migrate_users_saas_columns(connection) -> None:
    """Add SaaS owner-login fields while preserving legacy password users."""
    columns = _table_columns(connection, "users")
    additions = {
        "email": "TEXT",
        "full_name": "TEXT",
        "google_id": "TEXT",
        "auth_provider": "TEXT NOT NULL DEFAULT 'password'",
        "is_active": "BOOLEAN NOT NULL DEFAULT TRUE",
        "updated_at": "TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP",
    }
    for name, definition in additions.items():
        if name not in columns:
            connection.execute(f"ALTER TABLE users ADD COLUMN {name} {definition}")

    if "password_hash" in columns:
        connection.execute("ALTER TABLE users ALTER COLUMN password_hash DROP NOT NULL")

    connection.execute(
        """
        UPDATE users
        SET full_name = TRIM(COALESCE(first_name, '') || ' ' || COALESCE(last_name, ''))
        WHERE full_name IS NULL OR TRIM(full_name) = ''
        """
    )
    connection.execute(
        """
        UPDATE users
        SET email = NULLIF(TRIM(email), '')
        WHERE email IS NOT NULL
        """
    )
    connection.execute(
        """
        UPDATE users
        SET is_active = active
        WHERE is_active IS DISTINCT FROM active
        """
    )
    connection.execute(
        """
        UPDATE users
        SET auth_provider = 'password'
        WHERE auth_provider IS NULL OR auth_provider NOT IN ('password', 'google')
        """
    )
    connection.execute(
        """
        CREATE UNIQUE INDEX IF NOT EXISTS idx_users_email_lower
        ON users(LOWER(email))
        WHERE email IS NOT NULL
        """
    )
    connection.execute(
        """
        CREATE UNIQUE INDEX IF NOT EXISTS idx_users_google_id
        ON users(google_id)
        WHERE google_id IS NOT NULL
        """
    )


