    rows = connection.execute(
        """
        SELECT business_id, user_id, member_role, joined_at
        FROM business_members
        """
    ).fetchall()
    for row in rows:
        role = row["member_role"]
        if role not in {"owner", "admin", "employee"}:
            role = "employee"
        connection.execute(
            """
            INSERT INTO business_users
                (business_id, user_id, role, status, created_at)
            VALUES (%s, %s, %s, 'active', COALESCE(%s, CURRENT_TIMESTAMP))
            ON CONFLICT (business_id, user_id) DO UPDATE SET
                role = CASE
                    WHEN business_users.role = 'owner' THEN 'owner'
                    ELSE EXCLUDED.role
                END,
                status = 'active',
                updated_at = CURRENT_TIMESTAMP
            """,
            (row["business_id"], row["user_id"], role, row["joined_at"]),
        )


